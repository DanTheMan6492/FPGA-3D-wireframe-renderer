        reg   [15:0] expected;
        reg   [15:0] diff;
        begin
            a = av; b = bv;
            expected = ftz(ref_expected);
            #1;
            total = total + 1;
            if (result !== expected) begin
                // Compute approximate LSB distance for diagnostics
                if (result > expected) diff = result - expected;
                else                   diff = expected - result;
                if (diff <= 16'd1) mismatches_1lsb = mismatches_1lsb + 1;
                if (errors < 10) begin
                    $display("FAIL a=%h b=%h got=%h exp=%h refRaw=%h diff=%0d",
                        av, bv, result, expected, ref_expected, diff);
                end
                errors = errors + 1;
            end
        end
    endtask

